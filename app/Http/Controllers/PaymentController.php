    {
        $request->validate([
            'transaction_number' => 'required',
        ]);


        $payment = Payment::create($request->all());

        $payment_id = $payment->id;

        return redirect(route('order.create',['payment_id' => $payment_id]))->with('success','Payment saved successfully.');
    }

